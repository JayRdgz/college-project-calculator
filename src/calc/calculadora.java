    public static String calc(String mem1, String mem2, String sign) {
        
        Double result = 0.0;
        String response;
        
        if (sign.equals("+")) {
            result = Double.parseDouble(mem1) + Double.parseDouble(mem2);
        }
        if (sign.equals("-")) {
            result = Double.parseDouble(mem1) - Double.parseDouble(mem2);
        }
        if ( sign.equals("*") ) {
            result = Double.parseDouble(mem1) * Double.parseDouble(mem2);
        }
        if ( sign.equals("/") ) {
            result = Double.parseDouble(mem1) / Double.parseDouble(mem2);
        }
        
        response = result.toString();
        return response;
        
    }
    
    
    public static boolean existdot(String cad) {
        boolean result;
        result = false;
        
        for ( int i = 0; i < cad.length(); i++ ) {
            if ( cad.substring(i, i + 1).equals(".") ) {
                result = true;
                break;
            }
        }
        return false;
    }
    
    
    
    
